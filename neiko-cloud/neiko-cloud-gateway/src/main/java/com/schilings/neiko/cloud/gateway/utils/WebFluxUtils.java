package com.schilings.neiko.cloud.gateway.utils;

import com.schilings.neiko.common.model.constants.GeneralConstants;
import com.schilings.neiko.common.model.result.R;
import com.schilings.neiko.common.util.json.JsonUtils;
import org.springframework.core.io.buffer.DataBuffer;
import org.springframework.http.HttpHeaders;
import org.springframework.http.HttpStatus;
import org.springframework.http.MediaType;
import org.springframework.http.server.reactive.ServerHttpResponse;
import reactor.core.publisher.Mono;

/**
 * WebFlux 工具类
 *
 * @author Lion Li
 */
public class WebFluxUtils {

	/**
	 * 设置webflux模型响应
	 * @param response ServerHttpResponse
	 * @param value 响应内容
	 * @return Mono<Void>
	 */
	public static Mono<Void> webFluxResponseWriter(ServerHttpResponse response, Object value) {
		return webFluxResponseWriter(response, HttpStatus.OK, value, GeneralConstants.SUCCESS);
	}

	/**
	 * 设置webflux模型响应
	 * @param response ServerHttpResponse
	 * @param code 响应状态码
	 * @param value 响应内容
	 * @return Mono<Void>
	 */
	public static Mono<Void> webFluxResponseWriter(ServerHttpResponse response, Object value, int code) {
		return webFluxResponseWriter(response, HttpStatus.OK, value, code);
	}

	/**
	 * 设置webflux模型响应
	 * @param response ServerHttpResponse
	 * @param status http状态码
	 * @param code 响应状态码
	 * @param value 响应内容
	 * @return Mono<Void>
	 */
	public static Mono<Void> webFluxResponseWriter(ServerHttpResponse response, HttpStatus status, Object value,
			int code) {
		return webFluxResponseWriter(response, MediaType.APPLICATION_JSON_VALUE, status, value, code);
	}

	/**
	 * 设置webflux模型响应
	 * @param response ServerHttpResponse
	 * @param contentType content-type
	 * @param status http状态码
	 * @param code 响应状态码
	 * @param value 响应内容
	 * @return Mono<Void>
	 */
	public static Mono<Void> webFluxResponseWriter(ServerHttpResponse response, String contentType, HttpStatus status,
			Object value, int code) {
		response.setStatusCode(status);
		response.getHeaders().add(HttpHeaders.CONTENT_TYPE, contentType);
		R<?> result = R.fail(code, value.toString());
		DataBuffer dataBuffer = response.bufferFactory().wrap(JsonUtils.toJson(result).getBytes());
		return response.writeWith(Mono.just(dataBuffer));
	}

}
